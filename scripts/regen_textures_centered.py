def bounds_info(img):
    px = img.load()
    xs = [x for x in range(16) for y in range(16) if px[x,y][3]>0]
    ys = [y for x in range(16) for y in range(16) if px[x,y][3]>0]
    if xs:
        return "x=%d-%d y=%d-%d cx=%.1f cy=%.1f" % (min(xs),max(xs),min(ys),max(ys),(min(xs)+max(xs))/2,(min(ys)+max(ys))/2)
    return "empty"

def make_from_dots(dots_list, path):
    img = Image.new("RGBA", (16,16), (0,0,0,0))
    px  = img.load()
    for x, y, c in dots_list:
        px[x,y] = c
    img.save(path, "PNG")
    print("  %s  %s" % (os.path.basename(path), bounds_info(img)))

